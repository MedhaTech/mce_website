              <p class="text-justify">Alumni Association&nbsp;was formed during mid eighties and started with one chapter at Bangalore. The association has an Executive Council having 12 members along with a President and Secretary. The Executive Council has formulated the objectives and activities of the association, which include both short term and long-term goals of the association. It has also formulated the Constitution of the MCE Alumni Association along with bylaws for its effectiveness and sustainability.</p>
              <p class="text-justify">Our alumni network is very strong and the bonding between the alumni and the institution is reflected in various scholarships instituted by the alumni groups given to the students studying in the college and children studying anywhere, who are from economically weaker families. Laptops will be given to meritorious students. Labs have been equipped with IoT components with the help of alumni. Apart from these many other initiatives have been taken up by our proud alumni to motivate the current students towards greater achievements.</p>
              <p class="text-justify">Regular annual alumni meet titled &ldquo;NENAPINA DONI&rdquo; is organized every year on the second Saturday of every November. Huge number of alumni gathers and interacts with the final and pre final year students. This helps the students to know about the current industry needs and prepare themselves for placements. Many alumni have initiated campus placements and are successful in bringing the companies for campus hiring.</p>
              <p class="text-justify">Apart from NENAPINA DONI, regular alumni meets are organized across the globe and discussions take place to contribute something to the college. To name a few San Francisco meet in 2016, Karavali meet in Mangalore during 2017, Bangalore mega meet in 2018, New Jersey meet in 2019.</p>
              <p class="text-justify">A group of alumni have started funding and mentoring innovative projects carried out by the students. This has further strengthened the bond between the alumni and the college. Recently an alumni tower is proposed to be constructed in front of the library. Being a multi crore project, it will have a seminar hall, auditorium and few commercial components in it.</p>
              <p class="text-justify">In all, having a very strong alumni network, MCE is achieving many things related to academics, placements and the overall growth of the institution.</p>
