                  <h3>LITERARY CLUB</h3>
                  <p class="text-justify">The Literary Club (LIT) was established in 1993 and is one of the oldest and most pivotal clubs at Malnad College of Engineering. The club comprises students from all years making it a glory to carry on further. The club focuses on celebrating the interests of each and every person. </p>
