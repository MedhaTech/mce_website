                <div class="col-md-12"> 
                  <h6 class="text-underline">What We Do:</h6>
                  <p class="text-justify">The Literary Club organizes “THE MALNAD FEST”, the college’s annual fest, featuring over 35 sub-events. It is celebrated as the biggest fest in the college. It is organized during the even semester and it happens for 2 to 3 days. The fest starts with  a spectacular event called “ETHNIC DAY”. This is the most loved part of the fest as each and everyone dresses up and uplifts the culture and heritage of the nation. Prior to the days of the fest involves lots of qualifying rounds for the D-Day.  </p>
                  
                <p><b>The events are divided into four categories: </b></p>
                <p><b>Balwaan </b> – A challenge for sports enthusiasts to compete among different branches and emerge victorious. This category consists of sub events like Pentathlon, Tug of War and many more exciting events to test the strongest emperor in the college.  </p>
                <p><b>Buddhimaan </b> – A contest for intellectual minds to showcase their ultimate ingenuity. This category consists of events like Toastmaster, Literati, Quiz etc. It is a testament to the mind to solve mind-boggling questions ranging from current affairs to solving a mystery.  </p>
                <p><b>Darpan </b> - A stage event uniting the college through music and dance performances. It involves Group Singing, UV foot-lose, Vogue and many more. It gives all the participants a chance to dress up and showcase their talents in vivid imagery for the entire college to witness. </p>
                <p><b>Kalakruthi </b> – A platform to test and display artistic creativity. This focuses on the way the participants tell us a story using their brushes involving events like Pot Painting, Chithrakatha and much more.  </p>
                
               <p>As an inter-branch fest, teams compete to win the prestigious “Sarvottam” title. On the concluding day of the grand finale we organise a DJ night, an attempt to bring the college together. The fest attracts an audience of over 2,500 people annually, and the numbers continue to grow. </p>
				       <h4>NOESIS </h4>
