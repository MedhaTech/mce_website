              <p>To enhance placement opportunities, a dedicated placement office has also been set up in Bangalore.The office is well-equipped with an exclusive boardroom and five panel rooms, providing all necessary facilities to conduct campus recruitment programs. </p>

              <p><b>Student Enrollment and Placement Process</b></p>

              <p>Student enrollment for placement begins at the start of the fifth semester. A comprehensive database of enrolled students is maintained to facilitate hiring programs. Following enrollment, department-wise orientation sessions are conducted to highlight the significance of placements and related training.</p>

              <p>For the academic year 2024-25, till date<strong> 351 students have been successfully placed</strong> in reputed companies.</p>

              <p><b>Training and Skill Development Initiatives</b></p>

              <ul>
              To prepare students for recruitment, a series of structured training programs are implemented:
                <li> An introductory session on career growth, planning, and available opportunities is conducted to the first year students. </li>
                <li> Recognizing the importance of communication skills, a <strong>mandatory credit course in Professional English </strong> is introduced in the first year, followed by training in communication and soft skills in subsequent years.</li>
                <li> A three-day <strong>"FEEL Employable"</strong> program is conducted in collaboration with <strong>CLHRD, Mangalore</strong> to enhance students' soft skills.</li>
                <li> A two-week intensive <strong>aptitude and soft skills training</strong> is organized after the sixth-semester exams, conducted by expert trainers. This program is also a mandatory credit course</li>
                <li> A specialized crash course in <strong> C and C++ </strong>is provided to students from non-circuit branches to meet software industry requirements.</li>
                <li> Guest lectures by industry experts are regularly arranged to keep students updated on the latest technological advancements.</li>
              
              </ul>

              <p><b>Campus Recruitment and Higher Education Guidance</b></p>
              <p> The campus hiring programs have been highly successful, with top companies like TCS, HCL, Wipro, BEL, Accenture, Tech Mahindra, Tata Elxsi, Rane Industries, Goodrich Aerospace, Nokia Siemens Networks, Mindtree, Flowserve India Controls, TDPS, Ramky Infrastructures, L&T (IES)Schneider, Hive Minds, Incresco, Voya India, Infosys Finacle, Yokogawa, Honey Well and many more  recruiting students. </p>
              <p>Beyond placements, the department conducts awareness sessions on higher education opportunities in India and abroad. Expert lectures highlight the importance of GATE, GRE, TOEFL, IELTS, and other competitive exams to encourage students to explore further studies.</p>

              <p><b>Support for Students</b></p>
              <ul>
                <li> The TPO maintains a repository of previous years' aptitude test questions, interview puzzles, and placement-related materials, encouraging students to practice thoroughly.</li>
                <li> The laboratory is used for competitive exams on weekends, generating revenue for the institute.</li>
                <li> Students are encouraged to attend pool campus recruitment drives and VTU-organized placements, increasing their job opportunities.</li>
                <li> Since 2018, a Placement Awareness Talk is delivered during the induction program for newly admitted students. This session provides insights into career options, placements, higher studies, and competitive exams, ensuring students start preparing early for their future.</li>
              
              
              </ul>

              <p>Through these initiatives, the Training and Placement office remains committed to enhance students' employability and bridging the gap between academia and industry.</p>


